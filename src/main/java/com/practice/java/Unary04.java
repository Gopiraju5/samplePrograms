package com.practice.java;

public class Unary04 {
    public static void main(String[] args) {
        int x = 15;
        System.out.println(++x + x++);
        System.out.println(x);
        System.out.println("----------------------");

        int y = 15;
        System.out.println(++y + y++);
        System.out.println(y);
    }
}
