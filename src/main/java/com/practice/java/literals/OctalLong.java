package com.practice.java.literals;

public class OctalLong {
    public static void main(String[] args) {
        long l1 = 20L;
        long l2 = 020L;
        long l3 = 0XaddL;
        long l4 = 0b1010L;
        System.out.println(l1);
        System.out.println(l2);
        System.out.println(l3);
        System.out.println(l4);
    }
}
